/**
 * RAPENGINE — Auto-generate entity registry
 *
 * Parsuje MDX frontmatter a generuje src/lib/interlinking.ts
 * Spouští se automaticky před každým buildem (prebuild hook).
 *
 * Přidáš nový MDX soubor → registr se aktualizuje při příštím buildu.
 */

import { readdirSync, readFileSync, writeFileSync, existsSync } from 'fs'
import path from 'path'

const ROOT = path.resolve(process.cwd())

/**
 * Parse a frontmatter field value.
 * Handles: field: "value", field: 'value', field: value
 */
function parseField(content, field) {
  const m = content.match(new RegExp(`^${field}:\\s*(.+)$`, 'm'))
  if (!m) return null
  let value = m[1].trim()
  // Strip matching surrounding quotes (single or double)
  if (
    (value.startsWith('"') && value.endsWith('"')) ||
    (value.startsWith("'") && value.endsWith("'"))
  ) {
    value = value.slice(1, -1)
  }
  return value || null
}

/**
 * Parse a YAML list frontmatter field (e.g. aliases: \n  - "a"\n  - "b")
 * Returns array of strings, or null
 */
function parseList(content, field) {
  const lines = content.split('\n')
  let inField = false
  const values = []
  for (const line of lines) {
    if (line.startsWith(`${field}:`)) {
      inField = true
      // inline array: aliases: ["a", "b"]
      const inline = line.slice(field.length + 1).trim()
      if (inline.startsWith('[')) {
        try { return JSON.parse(inline.replace(/'/g, '"')) } catch {}
      }
      // Single inline value: aliases: "value"
      if (inline && !inline.startsWith('-')) {
        values.push(inline.replace(/^["']|["']$/g, ''))
        break
      }
      continue
    }
    if (!inField) continue
    // End of list — next top-level field
    if (!line.startsWith(' ') && !line.startsWith('\t') && line.trim() !== '') break
    const item = line.trim().replace(/^-\s*/, '').replace(/^["']|["']$/g, '')
    if (item) values.push(item)
  }
  return values.length > 0 ? values : null
}

function parseBool(content, field) {
  const m = content.match(new RegExp(`^${field}:\\s*(true|false)`, 'm'))
  return m?.[1] === 'true'
}

// Typy entit co se linkují v textu + cesta ke slugu
const ENTITY_TYPES = [
  { dir: 'content/raperi', type: 'rapper', aliasField: 'realName', caseSensitive: true },
  { dir: 'content/alba',   type: 'album',  aliasField: null,       caseSensitive: true },
  { dir: 'content/labely', type: 'label',  aliasField: null,       caseSensitive: true },
  { dir: 'content/zanry',  type: 'zanr',   aliasField: null,       caseSensitive: false },
]

const entries = []
const skipped = []

for (const { dir, type, aliasField, caseSensitive } of ENTITY_TYPES) {
  const dirPath = path.join(ROOT, dir)
  if (!existsSync(dirPath)) continue

  const files = readdirSync(dirPath).filter(f => f.endsWith('.mdx')).sort()

  for (const file of files) {
    const content = readFileSync(path.join(dirPath, file), 'utf8')

    const slug = parseField(content, 'slug')
    const title = parseField(content, 'title')
    if (!slug || !title) {
      console.warn(`  ⚠ Skipping ${file}: missing slug or title`)
      skipped.push({ file, type, missingSlug: !slug, missingTitle: !title })
      continue
    }

    const entry = { name: title, slug, type }
    if (caseSensitive === false) entry.caseSensitive = false

    // Build aliases from realName + explicit aliases list
    const allAliases = []
    if (aliasField) {
      const realName = parseField(content, aliasField)
      if (realName && realName !== title) allAliases.push(realName)
    }
    // Explicit aliases field in frontmatter
    const explicitAliases = parseList(content, 'aliases')
    if (explicitAliases) {
      for (const a of explicitAliases) {
        if (a !== title && !allAliases.includes(a)) {
          allAliases.push(a)
        }
      }
    }
    if (allAliases.length > 0) entry.aliases = allAliases

    entries.push(entry)
  }
}

// Seřaď abecedně pro přehlednost
entries.sort((a, b) => a.type.localeCompare(b.type) || a.name.localeCompare(b.name, 'cs'))

const byType = entries.reduce((acc, e) => {
  acc[e.type] = (acc[e.type] || 0) + 1
  return acc
}, {})

const generated = `// ═══════════════════════════════════════════════════════════════
// AUTO-GENERATED by scripts/generate-registry.mjs
//
// NEUPRAVUJ ručně — změny se přepíší při příštím buildu.
// Přidej rapper/album/label/zanr do content/ → automaticky se zalinkuje.
//
// Vygenerováno: ${new Date().toISOString()}
// Celkem entit: ${entries.length} (${Object.entries(byType).map(([k,v]) => `${v} ${k}`).join(', ')})
// ═══════════════════════════════════════════════════════════════

import type { InterlinkEntity } from './remark-interlinking'

export const ENTITY_REGISTRY: InterlinkEntity[] = ${JSON.stringify(entries, null, 2)}

export function getRegistryStats() {
  const byType = ENTITY_REGISTRY.reduce<Record<string, number>>((acc, e) => {
    acc[e.type] = (acc[e.type] || 0) + 1
    return acc
  }, {})
  return {
    total: ENTITY_REGISTRY.length,
    byType,
    totalAliases: ENTITY_REGISTRY.reduce((sum, e) => sum + (e.aliases?.length || 0), 0),
  }
}
`

const outPath = path.join(ROOT, 'src/lib/interlinking.ts')
writeFileSync(outPath, generated, 'utf8')

console.log(`✓ Registry vygenerován: ${entries.length} entit`)
Object.entries(byType).forEach(([k, v]) => console.log(`  ${k}: ${v}`))

if (skipped.length > 0) {
  console.log(`\n⚠ ${skipped.length} souborů přeskočeno:`)
  for (const s of skipped) {
    const missing = []
    if (s.missingSlug) missing.push('slug')
    if (s.missingTitle) missing.push('title')
    console.log(`  ${s.file} (${s.type}) — chybí: ${missing.join(', ')}`)
  }
}
